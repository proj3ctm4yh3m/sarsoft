package org.sarsoft.common.controller;

public class JSONForm {

	private String json;
	private String file;

	public void setJson(String json) {
		this.json = json;
	}

	public String getJson() {
		return json;
	}

	public void setFile(String file) {
		this.file = file;
	}

	public String getFile() {
		return file;
	}

}
